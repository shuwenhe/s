        loss_history: new float[20],
        grad_norm: 0.0,
        learning_rate: 0.001,
        epoch_time_ms: 0,
        total_time_ms: 0,
        tokens_per_second: 0.0,
    }
}

func make_config_snapshot(int vocab, int embed, int heads, int ffn, 
                            int layers, int seq_len, float dropout, int total_params) ModelConfigSnapshot {
    ModelConfigSnapshot {
        vocab_size: vocab,
        embed_dim: embed,
        num_heads: heads,
        ffn_dim: ffn,
        num_layers: layers,
        max_seq_len: seq_len,
