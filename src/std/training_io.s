package std.training_io

use std.tensor_core as T

struct CheckpointMeta {
    string format_version
    string framework
    string timestamp
    string s_compiler_version
}

struct TrainState {
    int global_step
    float current_loss
    float best_loss
    int best_step
    bool training_complete
    float[] loss_history

    float grad_norm
    float learning_rate
    int epoch_time_ms
    int total_time_ms
    float tokens_per_second
}

struct ModelConfigSnapshot {
    int vocab_size
    int embed_dim
    int num_heads
    int ffn_dim
    int num_layers
    int max_seq_len
    float dropout_prob

    int total_param_count
    int trainable_param_count
}

struct checkpoint {
    CheckpointMeta meta
    TrainState state
    ModelConfigSnapshot config

    Map<string, T.Tensor> weight_map

    string file_path
    long file_size_bytes
    string checksum_md5
}

func default_meta() CheckpointMeta {
    CheckpointMeta {
        format_version: "2.0",
        framework: "S-AI-Lib-v1.0",
        timestamp: _get_timestamp(),
        s_compiler_version: "1.0-enhanced"
    }
}

