// 整数转字符串
func int_to_str(int n) string {
    if n == 0 { return "0" }
    bool neg = n < 0
    if neg { n = -n }
    string s = ""
    while n > 0 {
        s = string((n % 10) + 48) + s
        n = n / 10
    }
    if neg { s = "-" + s }
    s
}

// 浮点格式化
func fmt_float(float val, int decimals) string {
    int ival = val as int
    float frac = val - ival as float
    if frac < 0 { frac = -frac }
    string result = int_to_str(ival)
    if decimals > 0 {
        result = result + "."
        int d = 0
        while d < decimals {
            frac = frac * 10.0
            int digit = frac as int
            result = result + int_to_str(digit)
            frac = frac - digit as float
            d = d + 1
        }
    }
    result
}

// 布尔转字符串
func bool_to_str(bool b) string {
    if b { "true" } else { "false" }
}

// 字符串是否以指定后缀结尾
func ends_with(string s, string suffix) bool {
    int slen = len(s)
    int suflen = len(suffix)
    if suflen > slen { return false }
    int i = 0
    while i < suflen {
        if s[slen - suflen + i] != suffix[i] { return false }
        i = i + 1
    }
    true
}

// 按行分割字符串
func split_lines(string text) string[] {
    // 简单实现: 统计行数然后分配
    int count = 1
    int i = 0
    while i < len(text) {
        if text[i] == char(10) { count = count + 1 }
        i = i + 1
    }
    
    string[] result = new string[count]
    string current = ""
    int idx = 0
    i = 0
    while i < len(text) {
        if text[i] == char(10) {
            result[idx] = current
            current = ""
            idx = idx + 1
        } else {
            current = current + string(text[i])
        }
        i = i + 1
    }
    if len(current) > 0 { result[idx] = current }
    result
}

// 从文本中提取整数字段的值
func parse_int_field(string text, string field_name, int default_val) int {
    // 查找 "field_name=value" 模式
    string target = field_name + "="
    int pos = find_substr(text, target)
    if pos < 0 { return default_val }
    
    pos = pos + len(target)
    string val_str = ""
    while pos < len(text) {
        char c = text[pos]
        if c == char(10) || c == char(13) || c == char(0) { break }
        val_str = val_str + string(c)
        pos = pos + 1
    }
    
    // 解析整数
    int result = 0
    int sign = 1
    int vi = 0
    while vi < len(val_str) {
        char vc = val_str[vi]
        if vc == char(45) { sign = -1 }  // '-'
        else if vc >= char(48) && vc <= char(57) {
            result = result * 10 + ((vc as int) - 48)
        }
        vi = vi + 1
    }
    result * sign
}

// 从文本中提取浮点字段的值
func parse_float_field(string text, string field_name, float default_val) float {
    string target = field_name + "="
    int pos = find_substr(text, target)
    if pos < 0 { return default_val }
    
    pos = pos + len(target)
    string val_str = ""
    while pos < len(text) {
        char c = text[pos]
        if c == char(10) || c == char(13) || c == char(0) { break }
        val_str = val_str + string(c)
        pos = pos + 1
    }
    
    // 解析浮点数
    float result = 0.0
    float sign = 1.0
    float divisor = 10.0
    bool after_dot = false
    int vi = 0
    while vi < len(val_str) {
        char vc = val_str[vi]
        if vc == char(45) { sign = -1.0 }
        else if vc == char(46) { after_dot = true }
        else if vc >= char(48) && vc <= char(57) {
            int digit = (vc as int) - 48
            if after_dot { result = result + digit as float / divisor; divisor = divisor * 10.0 }
            else { result = result * 10.0 + digit as float }
        }
        vi = vi + 1
    }
    result * sign
}

// 子串查找
func find_substr(string haystack, string needle) int {
    int hlen = len(haystack)
    int nlen = len(needle)
    if nlen == 0 { return 0 }
    if nlen > hlen { return -1 }
    
    int i = 0
    while i <= hlen - nlen {
        bool found = true
        int j = 0
        while j < nlen {
            if haystack[i+j] != needle[j] { found = false; break }
            j = j + 1
        }
        if found { return i }
        i = i + 1
    }
    -1
}

// 数字填充到指定宽度 (左补空格)
func pad_int(int n, int width) string {
    string s = int_to_str(n)
    while len(s) < width { s = " " + s }
    s
}

// 浮点填充到指定宽度和小数位
func pad_float(float val, int width, int decimals) string {
    string s = fmt_float(val, decimals)
    while len(s) < width { s = " " + s }
    s
}

// ============================================
// File I/O Bridge (文件IO桥接层)
// 连接到 S 语言标准库 std.fs 的实际实现
// ============================================

// 使用 extern intrinsic 调用运行时的文件读写
// 这些函数在 S 编译器中被映射到实际的 host 函数
extern "intrinsic" func __host_write_text_file(string path, string contents) int
extern "intrinsic" func __host_read_to_string(string path) string
extern "intrinsic" func __host_file_exists(string path) int

// 包装写操作
func _write_file(string path, string content) WriteResult {
    int ret = __host_write_text_file(path, content)
    WriteResult { ok: ret >= 0, error_code: ret }
}

// 包装读操作
func _read_file(string path) ReadResult {
    if __host_file_exists(path) == 0 {
        return ReadResult { ok: false, data: "", error: "file not found" }
    }
    string data = __host_read_to_string(path)
    ReadResult { ok: true, data: data, error: "" }
}

// 结果类型包装
struct WriteResult {
    bool ok
    int error_code
}

struct ReadResult {
    bool ok
    string data
    string error
}

func result_is_ok(WriteResult r) bool { r.ok }
func result_is_ok(ReadResult r) bool { r.ok }
func get_result_string(ReadResult r) string { r.data }

// 单字符构造 (用于字符串处理)
func char(int code) string { string(code) }
