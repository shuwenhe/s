// ============================================
// checkpoint Format v2 (检查点格式)
// 
// 文件结构 (.neurx 格式):
// ╔══════════════════════════════╗
// ║ # NeurX checkpoint v2      ║  注释头
// ║ [metadata]                  ║  元数据段
// ║ format_version=2.0          ║
// ║ framework=S-AI-Lib          ║
// ║ timestamp=...               ║
// ║ [training]                  ║  训练状态段  
// ║ step=100                    ║
// ║ loss=0.123456               ║
// ║ best_loss=0.098765          ║
// ║ [model_config]              ║  模型配置段
// ║ vocab_size=256              ║
// ║ embed_dim=128               ║
// ║ num_layers=4                ║
// ║ total_parameters=825344     ║
// ║ [weights]                   ║  权重数据段 (Base64或文本)
// ║ token_embed.weight=...      ║
// ║ pos_embed.weight=...        ║
// ║ blocks.0.attn.q_proj=...    ║
// ║ [optimizer]                 ║  优化器状态
// ║ adam_step=100               ║
// ╚════════════════════════════╝
// ============================================

// ============================================
// Data Structures (数据结构)
// ============================================

// 检查点元数据
struct CheckpointMeta {
    string format_version       // "2.0"
    string framework            // "S-AI-Lib-v1"
    string timestamp             // ISO时间戳
    string s_compiler_version    // S编译器版本
}

// 训练快照状态
struct TrainState {
    int global_step             // 当前训练步数
    float current_loss           // 当前步的loss
    float best_loss              // 历史最佳loss
    int best_step                // 最佳loss出现的步数
    bool training_complete       // 是否完成训练
    float[] loss_history         // 近期loss历史 (滑动窗口)
    
    // 性能指标
    float grad_norm              // 当前梯度范数
    float learning_rate          // 当前学习率
    int epoch_time_ms            // 本epoch耗时(ms)
    int total_time_ms            // 总耗时(ms)
    float tokens_per_second     // 吞吐量
}

// 模型配置快照 (用于恢复时验证一致性)
struct ModelConfigSnapshot {
    int vocab_size
    int embed_dim
    int num_heads
    int ffn_dim
    int num_layers
    int max_seq_len
    float dropout_prob
    
    // 参数统计
    int total_param_count
    int trainable_param_count
}

// 完整的检查点结构
struct checkpoint {
    CheckpointMeta meta
    TrainState state
    ModelConfigSnapshot config
    
    // 权重数据 (名称->扁平化float数组)
    Map<string, T.Tensor> weight_map
    
    // 文件路径信息
    string file_path
    long file_size_bytes
    string checksum_md5         // 完整性校验 (可选)
}

