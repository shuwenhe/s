// 将完整checkpoint序列化为.neurx格式字符串
func serialize_checkpoint(checkpoint ckpt) string {
    string content = ""
    
    // ===== Header =====
    content = content + "# ============================================\n"
    content = content + "# NeurX checkpoint v2\n"
    content = content + "# Generated by S Language AI Runtime\n"
    content = content + "# Framework: " + ckpt.meta.framework + "\n"
    content = content + "# ============================================\n\n"
    
    // ===== [metadata] Section =====
    content = content + "[metadata]\n"
    content = content + "format_version=" + ckpt.meta.format_version + "\n"
    content = content + "framework=" + ckpt.meta.framework + "\n"
    content = content + "timestamp=" + ckpt.meta.timestamp + "\n"
    content = content + "s_version=" + ckpt.meta.s_compiler_version + "\n\n"
    
    // ===== [training] Section =====
    content = content + "[training]\n"
    content = content + "step=" + int_to_str(ckpt.state.global_step) + "\n"
    content = content + "loss=" + fmt_float(ckpt.state.current_loss, 8) + "\n"
    content = content + "best_loss=" + fmt_float(ckpt.state.best_loss, 8) + "\n"
    content = content + "best_step=" + int_to_str(ckpt.state.best_step) + "\n"
    content = content + "completed=" + bool_to_str(ckpt.state.training_complete) + "\n"
    content = content + "grad_norm=" + fmt_float(ckpt.state.grad_norm, 6) + "\n"
    content = content + "learning_rate=" + fmt_float(ckpt.state.learning_rate, 8) + "\n"
    content = content + "total_time_ms=" + int_to_str(ckpt.state.total_time_ms) + "\n"
    content = content + "throughput=" + fmt_float(ckpt.state.tokens_per_second, 1) + "\n"
    
    // Loss history as array
    content = content + "loss_history=["
    int i = 0
    while i < len(ckpt.state.loss_history) {
        if i > 0 { content = content + ", " }
        content = content + fmt_float(ckpt.state.loss_history[i], 4)
        i = i + 1
    }
    content = content + "]\n\n"
    
    // ===== [model_config] Section =====
    content = content + "[model_config]\n"
    content = content + "vocab_size=" + int_to_str(ckpt.config.vocab_size) + "\n"
    content = content + "embed_dim=" + int_to_str(ckpt.config.embed_dim) + "\n"
    content = content + "num_heads=" + int_to_str(ckpt.config.num_heads) + "\n"
    content = content + "ffn_dim=" + int_to_str(ckpt.config.ffn_dim) + "\n"
    content = content + "num_layers=" + int_to_str(ckpt.config.num_layers) + "\n"
    content = content + "max_seq_len=" + int_to_str(ckpt.config.max_seq_len) + "\n"
    content = content + "dropout=" + fmt_float(ckpt.config.dropout_prob, 3) + "\n"
    content = content + "total_params=" + int_to_str(ckpt.config.total_param_count) + "\n\n"
    
    // ===== [weights] Section =====
    content = content + "[weights]\n"
    // 遍历所有权重并序列化
    for name, tensor in ckpt.weight_map {
        content = content + name + ".shape=["
        int d = 0
        while d < tensor.shape.ndim {
            if d > 0 { content = content + "," }
            content = content + int_to_str(tensor.shape.dims[d])
            d = d + 1
        }
        content = content + "]\n"
        
        // 序列化数据值 (前N个用于预览，全部保存)
        content = content + name + ".data="
        int n = tensor.shape.size
        if n > 100 {
            // 大tensor只存前10和后10个值，中间用省略号
            int p = 0
            while p < 10 {
                content = content + fmt_float(tensor.data[p], 6)
                if p < n - 1 { content = content + "," }
                p = p + 1
            }
            content = content + "...(" + int_to_str(n - 20) + " more)..."
            p = n - 10
            while p < n {
                content = content + fmt_float(tensor.data[p], 6)
                if p < n - 1 { content = content + "," }
                p = p + 1
            }
        } else {
            int p = 0
            while p < n {
                content = content + fmt_float(tensor.data[p], 6)
                if p < n - 1 { content = content + "," }
                p = p + 1
            }
        }
        content = content + "\n"
    }
    content = content + "\n"
    
    // ===== Footer =====
    content = content + "# End of checkpoint\n"
    
    content
}

